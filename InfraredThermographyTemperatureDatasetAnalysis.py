import pandas as pd
import numpy as np
import seaborn as sns
import matplotlib.pyplot as plt

from sklearn.preprocessing import StandardScaler
from sklearn.ensemble import IsolationForest
from sklearn.decomposition import PCA
from sklearn.cross_decomposition import CCA
from factor_analyzer import FactorAnalyzer
from ucimlrepo import fetch_ucirepo

"""**Load Data**"""

infrared = fetch_ucirepo(id=925)

X = infrared.data.features.copy()
y = infrared.data.targets.copy()

print(X.head())
print(X.shape)

"""**Handling Missing Values**"""

print("\nMissing values:\n", X.isnull().sum())

X = X.fillna(X.mean(numeric_only=True))

"""**Label Encoding**"""

from sklearn.preprocessing import LabelEncoder

df = X.copy()

le_gender = LabelEncoder()
df['Gender'] = le_gender.fit_transform(df['Gender'])

le_age = LabelEncoder()
df['Age'] = le_age.fit_transform(df['Age'])

le_ethnicity = LabelEncoder()
df['Ethnicity'] = le_ethnicity.fit_transform(df['Ethnicity'])

y = y.iloc[:, 0]

"""**Boxplot (Before Outlier Removal)**"""

plt.figure(figsize=(12,6))
sns.boxplot(data=df)
plt.title("Before Outlier Removal")
plt.xticks(rotation=90)
plt.show()

"""**Outlier Removal**"""

iso = IsolationForest(contamination=0.05, random_state=42)

outliers = iso.fit_predict(df)
mask = outliers != -1

X_clean = df[mask].reset_index(drop=True)
y_clean = y.iloc[mask].reset_index(drop=True)

print("Original shape:", df.shape)
print("Clean shape:", X_clean.shape)

"""**Clean Data**"""

print("\n===== CLEANED DATA HEAD =====")
print(X_clean.head())

print("\n===== CLEANED DATA INFO =====")
print(X_clean.info())

print("\n===== CLEANED SHAPE =====")
print(X_clean.shape)

"""**Boxplot (After Outlier Removal)**"""

plt.figure(figsize=(12,6))
sns.boxplot(data=X_clean)
plt.title("After Outlier Removal")
plt.xticks(rotation=90)
plt.show()

scaler = StandardScaler()
X_scaled = scaler.fit_transform(X_clean)

X_scaled_df = pd.DataFrame(X_scaled, columns=X_clean.columns)

pca = PCA()
X_pca = pca.fit_transform(X_scaled)

plt.figure(figsize=(8,5))
plt.plot(range(1, len(pca.explained_variance_ratio_)+1),
         pca.explained_variance_ratio_,
         marker='o')

plt.title("Scree Plot")
plt.xlabel("Components")
plt.ylabel("Variance Explained")
plt.grid()
plt.show()

pca_80 = PCA(n_components=0.80)
X_pca_80 = pca_80.fit_transform(X_scaled)

print("Selected components:", pca_80.n_components_)

loadings = pca_80.components_.T * np.sqrt(pca_80.explained_variance_)

loadings_df = pd.DataFrame(
    loadings,
    index=X_clean.columns,
    columns=[f"PC{i+1}" for i in range(loadings.shape[1])]
)

plt.figure(figsize=(12,6))
sns.heatmap(loadings_df.T, cmap="coolwarm", center=0)
plt.title("PCA Loading Heatmap")
plt.show()

pca = PCA()
X_pca = pca.fit_transform(X_scaled)

# Cumulative variance
cumulative_variance = np.cumsum(pca.explained_variance_ratio_)

plt.figure(figsize=(8,5))
plt.plot(range(1, len(cumulative_variance)+1),
         cumulative_variance,
         marker='o')

plt.axhline(y=0.80, color='r', linestyle='--')
plt.title("Cumulative Explained Variance")
plt.xlabel("Number of Components")
plt.ylabel("Cumulative Variance Explained")
plt.grid()
plt.show()

loadings = pca.components_.T * np.sqrt(pca.explained_variance_)

loadings_df = pd.DataFrame(
    loadings,
    index=X_clean.columns,
    columns=[f"PC{i+1}" for i in range(loadings.shape[1])]
)

print(loadings_df)

# Top contributing features per PC
for i in range(min(6, loadings_df.shape[1])):  # first 6 PCs
    pc = f"PC{i+1}"
    print(f"\n🔹 {pc} - Top contributing variables:")

    print(loadings_df[pc]
          .abs()
          .sort_values(ascending=False)
          .head(5))

from factor_analyzer.factor_analyzer import calculate_kmo, calculate_bartlett_sphericity

kmo_all, kmo_model = calculate_kmo(X_clean)

print(f"KMO Overall Score: {kmo_model:.3f}")

chi_square_value, p_value = calculate_bartlett_sphericity(X_clean)

print(f"Bartlett’s test p-value: {p_value:.5f}")

fa = FactorAnalyzer(n_factors=3, rotation='varimax')

